    private void LoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtonMouseClicked
        String username = UsernameTextField.getText();
        String password = PasswordTextField.getText();

        boolean isAdmin = authRepositories.loginRepo(
            username, password);

        if (isAdmin) {
            DashboardView dashboardView = new DashboardView();
            
            dashboardView.setVisible(true);
            dashboardView.setResizable(false);
            dashboardView.pack();
            dashboardView.setLocationRelativeTo(null);
            dashboardView.setDefaultCloseOperation(dashboardView.EXIT_ON_CLOSE);

            this.setVisible(false);
        } else {
            JOptionPane
            .showMessageDialog(this,
                "Username atau Password anda salah, ulangi lagi !!..");
        }
    }//GEN-LAST:event_LoginButtonMouseClicked

