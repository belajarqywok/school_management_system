    private void TableHandler(String mode, String search) {
        tableModel = new DefaultTableModel();
        EmployeeTable.setModel(tableModel);
        
        String[] columnNames = {
            "Nomor",
            "NIP",
            "Nama",
            "Tgl. Lahir",
            "Jenis Kelamin",
            "Jenis Perkerjaan",
            "No. HP",
            "Alamat"
        };
        
        tableModel.setColumnIdentifiers(columnNames);
        
        List<EmployeeDto> employeeDataList;
        if (mode.equals("searching")) {
            employeeDataList = employeeRepositories
                .searchEmployeesRepository(search);
        } else {
            employeeDataList = employeeRepositories
                .getEmployeesRepository();
        }
        
        int index = 1;
        employeeIdLists = new ArrayList<>();
        for (EmployeeDto employeeData : employeeDataList) {
            String employeeId      = employeeData.getEmployeeId();
            String employeeNip     = employeeData.getEmployeeNip();
            String employeeName    = employeeData.getEmployeeName();
            String employeeBirth   = employeeData.getEmployeeBirthDate();
            String employeeGender  = employeeData.getEmployeeGender();
            String employeeType    = employeeData.getEmployeeType();
            String employeePhone   = employeeData.getEmployeePhone();
            String employeeAddress = employeeData.getEmployeeAddress();
            
            tableModel.addRow(new Object[]{
                index, employeeNip, employeeName, employeeBirth,
                employeeGender, employeeType, employeePhone, employeeAddress
            });
            
            employeeIdLists.add(employeeId);
            index ++;
        }
    }
    
