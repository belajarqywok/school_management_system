    private void EmployeeTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EmployeeTableMouseClicked
        int selectedRow = EmployeeTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane
                .showMessageDialog(this, 
                "Pilih baris yang ingin diupdate.");
        }
        
        CachingService.put("employeeId", employeeIdLists.get(
                (int) tableModel.getValueAt(selectedRow, 0) - 1
            )
        );
        
        EmployeeNipTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 1));
        EmployeeNameTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 2));
        EmployeeBirthTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 3));
        EmployeeGenderComboBox.setSelectedItem(
            (Object) tableModel.getValueAt(selectedRow, 4));
        EmployeeTypeComboBox.setSelectedItem(
            (Object) tableModel.getValueAt(selectedRow, 5));
        EmployeePhoneTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 6));
        EmployeeAddressTextArea.setText(
            (String) tableModel.getValueAt(selectedRow, 7));
    }//GEN-LAST:event_EmployeeTableMouseClicked

    private void AddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddButtonMouseClicked
        employeeDto.setEmployeeNip(EmployeeNipTextField.getText());
        employeeDto.setEmployeeName(EmployeeNameTextField.getText());
        employeeDto.setEmployeeBirthDate(EmployeeBirthTextField.getText());
        employeeDto.setEmployeeGender(EmployeeGenderComboBox.getSelectedItem().toString());
        employeeDto.setEmployeeType(EmployeeTypeComboBox.getSelectedItem().toString());
        employeeDto.setEmployeePhone(EmployeePhoneTextField.getText());
        employeeDto.setEmployeeAddress(EmployeeAddressTextArea.getText());
        
        Map<String, Object> createEmployee = employeeRepositories
            .createEmployeeRepository(employeeDto);
        
        postAction(
            (boolean) createEmployee.get("result"),
            (String) createEmployee.get("message")
        );
    }//GEN-LAST:event_AddButtonMouseClicked

