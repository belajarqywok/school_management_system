    private void SubjectTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SubjectTableMouseClicked
        int selectedRow = SubjectTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane
                .showMessageDialog(this, 
                "Pilih baris yang ingin diupdate.");
        }
        
        CachingService.put("subjectId", subjectIdLists.get(
                (int) tableModel.getValueAt(selectedRow, 0) - 1
            )
        );
        
        SubjectNameTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 1));
        SubjectDescriptionTextArea.setText(
            (String) tableModel.getValueAt(selectedRow, 2));
    }//GEN-LAST:event_SubjectTableMouseClicked

    private void AddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddButtonMouseClicked
        subjectDto.setSubjectName(SubjectNameTextField.getText());
        subjectDto.setSubjectDesc(SubjectDescriptionTextArea.getText());
        
        Map<String, Object> createSubject = subjectRepositories
            .createSubjectRepository(subjectDto);
        
        postAction(
            (boolean) createSubject.get("result"),
            (String) createSubject.get("message")
        );
    }//GEN-LAST:event_AddButtonMouseClicked

