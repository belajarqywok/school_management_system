    private void TableHandler(String mode, String search) {
        tableModel = new DefaultTableModel();
        SubjectTable.setModel(tableModel);
        
        String[] columnNames = {
            "Nomor",
            "Nama MaPel",
            "Deskripsi MaPel"
        };
        
        tableModel.setColumnIdentifiers(columnNames);
        
        List<SubjectDto> subjectDataList;
        if (mode.equals("searching")) {
            subjectDataList = subjectRepositories
                .searchSubjectsRepository(search);
        } else {
            subjectDataList = subjectRepositories
            .getSubjectsRepository();
        }
        
        int index = 1;
        subjectIdLists = new ArrayList<>();
        for (SubjectDto subjectData : subjectDataList) {
            String subjectId    = subjectData.getSubjectId();
            String subjectName  = subjectData.getSubjectName();
            String subjectDesc  = subjectData.getSubjectDesc();
            
            tableModel.addRow(new Object[]{
                index, subjectName, subjectDesc
            });
            
            subjectIdLists.add(subjectId);
            index ++;
        }
    }
    
    private void postAction(boolean condition, String message) {
        subjectDto = new SubjectDto();
        CachingService.clear();
        TableHandler("default", null);
        
        if (condition) {
            JOptionPane.showMessageDialog(
                this, 
                message,
                "Berhasil...",
                JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(
                this, 
                message,
                "Gagal...",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }

