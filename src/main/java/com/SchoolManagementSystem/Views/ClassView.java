    private void ClassesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ClassesTableMouseClicked
        int selectedRow = ClassesTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane
                .showMessageDialog(this, 
                "Pilih baris yang ingin diupdate.");
        }
        
        CachingService.put("classId", classIdLists.get(
                (int) tableModel.getValueAt(selectedRow, 0) - 1
            )
        );
        
        ClassNameTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 1));
        ClassCapacityTextField.setText(
            (String) tableModel.getValueAt(selectedRow, 2));
    }//GEN-LAST:event_ClassesTableMouseClicked

    private void AddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddButtonMouseClicked
        classesDto.setClassName(ClassNameTextField.getText());
        classesDto.setClassCapacity(ClassCapacityTextField.getText());
        
        Map<String, Object> createClass = classRepositories
            .createClassRepository(classesDto);
        
        postAction(
            (boolean) createClass.get("result"),
            (String) createClass.get("message")
        );
    }//GEN-LAST:event_AddButtonMouseClicked

