    private void TableHandler(String mode, String search) {
        tableModel = new DefaultTableModel();
        ClassesTable.setModel(tableModel);
        
        String[] columnNames = {
            "Nomor",
            "Nama Kelas",
            "Kapasitas Kelas"
        };
        
        tableModel.setColumnIdentifiers(columnNames);
        
        List<ClassesDto> classesDataList;
        if (mode.equals("searching")) {
            classesDataList = classRepositories
                .searchClassesRepository(search);
        } else {
            classesDataList = classRepositories
            .getClassesRepository();
        }
        
        int index = 1;
        classIdLists = new ArrayList<>();
        for (ClassesDto classData : classesDataList) {
            String classId         = classData.getClassId();
            String className       = classData.getClassName();
            String classCapacity   = classData.getClassCapacity();
            
            tableModel.addRow(new Object[]{
                index, className, classCapacity
            });
            
            classIdLists.add(classId);
            index ++;
        }
    }
    
    private void postAction(boolean condition, String message) {
        classesDto = new ClassesDto();
        CachingService.clear();
        TableHandler("default", null);
        
        if (condition) {
            JOptionPane.showMessageDialog(
                this, 
                message,
                "Berhasil...",
                JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(
                this, 
                message,
                "Gagal...",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }

